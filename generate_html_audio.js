function downloadTTSChunk(text) {
    const url = 'https://translate.googleapis.com/translate_tts?ie=UTF-8&q=' + encodeURIComponent(text) + '&tl=mr&client=tw-ob';
    return new Promise((resolve, reject) => {
        https.get(url, (res) => {
            const chunks = [];
            res.on('data', c => chunks.push(c));
            res.on('end', () => resolve(Buffer.concat(chunks)));
            res.on('error', reject);
        }).on('error', reject);
    });
}

async function downloadSlideAudio(chunks, filename) {
    const file = fs.createWriteStream(filename);
    for (let c of chunks) {
        const buffer = await downloadTTSChunk(c);
        file.write(buffer);
    }
    file.end();
    return new Promise(resolve => file.on('finish', resolve));
}

async function main() {
    console.log('Generating exact MP3s for HTML slides...');
    for (let i = 0; i < narration.length; i++) {
        const outName = path.join('www', `slide${i + 1}.mp3`);
        console.log(`Downloading audio for slide ${i + 1}...`);
        await downloadSlideAudio(narration[i], outName);
        console.log(`Saved ${outName}`);
    }
    console.log('✅ All audio slides generated successfully!');
}

